<p>En presencia alumnos de la División de Ingenierías en Ciencias Agropecuarias, este viernes 21 de septiembre, la Universidad Tecnológica de la Costa firmó un Convenio de Colaboración con la Empresa  Innovaorgánicos Cultivos Supremos S.P.R. de C.V. de Morelia Michoacán, dejando las puertas abiertas para que alumnos de la Institución, principalmente de la Ingeniería en Agrobiotecnología,  puedan realizar prácticas y estadías.

 </p>

<p>En este encuentro ambas partes representadas por el L.A.E. Miguel Salinas Ramírez y el M.C. David García Hernández, acordaron estar en la mejor disposición de trabajar en proyectos que coadyuven a la formación de los futuros profesionistas de la Institución.  

</p>

<p>Destacar que Innovaorgánicos  es una empresa productora de insumos orgánicos que tiene como objetivo promover la investigación, aplicación de innovaciones, desarrollos tecnológicos transferencia de tecnología y conservación de los recursos fotogenéticos nativos de México.

 </p>
 
