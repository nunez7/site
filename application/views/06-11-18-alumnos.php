<p>Gracias al apoyo del Gobierno del Estado de Nayarit que encabeza el L.C.P. Antonio Echevarría García, los alumnos de turismo de UTC, Georgina Litzi Anahí Morales Gómez originaria de Santiago Ixcuintla y Juan Manuel Cárdenas Rosas de Amapa, Mpio. de Santiago Ixcuintla, harán historia al ser los primeros beneficiados de la Beca de Movilidad a Francia, AMESTUR 2018.
 </p>

<p>Destacar que del 15 de diciembre al 08 de febrero, tendrán una estancia con gastos pagados en un Resort Turístico en VTF Le Rafour y VTF Les Encontres Francia, desarrollando prácticas profesionales  de animación social, que sin duda enriquecerán su formación académica.
 </p>

<p>En este sentido el Rector de la Institución L.A.E. Miguel Salinas Ramírez dijo sentirse muy orgulloso de ambos jóvenes, por ser los primeros en beneficiarse de esta beca, asimismo, los invitó a aprovechar al máximo la experiencia y dejar las puertas abiertas a nuevas generaciones.
 </p>
 
