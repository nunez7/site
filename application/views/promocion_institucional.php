<p>Necesitamos estar actualizados en las  estrategias para la promoción 
institucional, estrategias que nos permitan ser cada día más competitivos, 
así lo manifestó el <strong>Rector de la Universidad Tecnológica de la Costa</strong>, 
<strong>Ing. Manuel Quintero Borrego</strong> en la apertura del proceso de capacitación 
para la Promoción Institucional 2014, que inició este jueves 10 de octubre 
con el Curso Taller “<em>Herramientas de Promoción</em>”.</p>

<p>Se contó con la asistencia de <strong>Personal Directivo y Administrativo</strong>, 
teniendo como objetivo principal: Proporcionar al personal de la Universidad 
Tecnológica de la Costa las Herramientas necesarias que les permitan 
difundir la oferta y modelo educativo de la Institución, así como la 
importancia que tiene el estudiar una carrera en una Universidad  de calidad 
como esta.</p>

<p class="reduced">Al concluir este evento coordinado por <strong>Departamento de Extensión, 
Difusión y Divulgación Universitaria de la Secretaría de Vinculación</strong>, 
 Quintero Borrego, con la entrega de un merecido reconocimiento, agradeció 
 a los asistentes el compromiso que hasta la fecha han asumido a favor de la 
 Universidad Tecnológica de la Costa, e hizo lo propio con el <strong>Lic. 
  Francisco Quintero Castillo</strong>, Asesor Financiero de <strong>Grupo INBURSA</strong>, a quien 
 agradeció la impartición de este curso taller que sin duda será beneficioso 
 para la Institución. </p>