<p>Este jueves 21 de junio el Rector de la Universidad Tecnológica de la Costa L.A.E. Miguel Salinas Ramírez, acompañado del Cuerpo Directivo de la Institución sostuvo una reunión para felicitar al  M.E.A. Pedro Vázquez Vázquez y la Ing. Merle Catalina Navarro Cortez P.T.C. de la División de Ingenierías en Ciencias Agropecuarias, por la publicación realizada en la Revista Internacional “African Journal of Agricultural Research”, con el  artículo: “Uso de Alternativas Orgánicas en el Sistema de Producción de Chile Habanero bajo Condiciones de Invernadero”.

 </p>

<p>Reconoció el esfuerzo y dedicación de ambos y dijo sentirse orgulloso de que la Institución destaque a nivel internacional por la calidad de sus Docentes y más aún por que el conocimiento será compartido con alumnos de la Ingeniería en Agrobiotecnología, ya que va encaminado obtener información sobre la nutrición del cultivo de chile habanero, a través de abono orgánico producto del  estiércol bovino.

</p>

<p>Agregar que es la tercera vez que el Maestro Pedro publica en la revista y anteriormente a tenido como colaboradores al Dr. Arnoldo Wong Villarreal de la UT de la Selva de Ocosingo Chiapas y al Dr. David García Hernández de la UT de Morelia.
 </p>
