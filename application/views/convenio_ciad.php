<p>Reconociendo la importancia de estar relacionados con el Sector 
Productivo y de esta forma coadyuvar en la formación académica  
de los alumnos de la Institución, este lunes 21  de octubre, en 
la apertura de la <strong>20ª Semana Nacional de Ciencia y Tecnología 2013</strong>, 
se llevó a cabo la firma de un Convenio de Colaboración entre  la <strong>Universidad 
Tecnológica de la Costa</strong> y el <strong>Centro de Investigación en Alimentos y 
Desarrollo A.C. (CIAD) Tepic</strong>.</p>
<p>Los encargados de firmar este documento fueron  el <strong>Ing. Ignacio Manuel 
Quintero Borrego</strong>, en su carácter de Rector y  el Dr. Gerardo Torres García 
Responsable de la Oficina de Gestión del CIAD.</p>
<p>Derivado de lo anterior alumnos  de la Universidad Tecnológica de la Costa 
podrán acudir a  las diferentes áreas del CIAD a realizar estadías, 
escuelas prácticas, estancias, visitas de inducción y recorridos por 
las diferentes áreas que se requieran, además permitirá realizar cursos, 
talleres, conferencias, seminarios, encuentro, congresos, proyectos de 
investigación y desarrollo de tecnología, desarrollo de programas 
académicos de acreditación conjunta, intercambio de personal con fines 
académicos o de asesoramiento, entre otros beneficios más, que permitirán 
optimizar recursos a través de la ayuda mutua, el trabajo coordinado y 
el intercambio de experiencias.</p>
<p class="reduced">Con la firma de este convenio queda demostrada la importancia que tiene 
vincular al Sector Productivo en el proceso de formación de los  futuros 
profesionistas de la Universidad Tecnológica de la Costa y así unidos, 
coadyuvar en el desarrollo de la Zona Norte, del Estado y del País.
</p>