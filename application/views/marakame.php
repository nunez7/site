<p>El Gobierno de la Gente a través del Instituto <strong>MARAKAME</strong> en coordinación con el Área 
	Médico Psicológica de la <strong>Universidad Tecnológica de la Costa</strong> hicieron posible que este 
	miércoles 25 de septiembre se llevaran a cabo en la Institución una serie de 
	conferencias relacionadas con la prevención de adicciones, impartidas por la Lic. 
	Jesabel Gallegos de Dios, Subdirectora, Lic. Vianey Martínez Mendoza, Nutriólogo; 
	y el Lic. Jorge Alexis Jiménez López, Terapeuta de Campo del Instituto <strong>MARAKAME</strong> del 
	Estado de Nayarit, quienes trataron temas como: <strong>Alcoholismo</strong>, 
	<strong>Tabaquismo</strong>, <strong>Drogadicción</strong> 
	y <strong>Desórdenes Alimentarios</strong>.</p>
<p>En su mensaje inaugural de dichas conferencias el <strong>Ing. Manuel Quintero 
	Borrego, Rector 
	de la Universidad Tecnológica de la Costa</strong>, dio la bienvenida a personal de 
	<strong>MARAKAME</strong> y 
	exhortó a la comunidad Universitaria a compartir los conocimientos adquiridos y así, 
	ser promotores de la prevención de adicciones, frenando a este monstruo que cada vez 
	cobra más vidas.</p>
<p class="reduced">Concluyó: <em>“Quiéranse para que puedan querer a los demás, protéjanse contra las adicciones 
	y la mejor protección es la información”</em>.</p>