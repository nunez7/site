<p>Este jueves 10 de octubre con una jornada de <strong>Talleres Literarios y la 
presentación de la Revista  “La Jerga”</strong>, la <strong>Universidad Tecnológica de la 
Costa</strong> festejó el Primer Aniversario de la “<em>Sala de Lectura La piedra en 
el Estanque</em>”, creada en la Institución  con el propósito de aumentar 
el nivel de lectura en la Comunidad Universitaria.</p>
<p>Destacar que actualmente la UT  de la Costa es la única Institución 
Educativa en el Estado que cuenta con una sala de lectura reconocida 
por <strong>CONACULTA</strong> y la <strong>SEP</strong>, para su creación se  
aplicó un recurso de 
$140,000 del Programa de Apoyo al Desarrollo de la Educación Superior 
(<strong>PADES</strong>) y es parte del proyecto Universidad Lectora.</p>
<p>Las actividades iniciaron a  primera hora con una serie de  <strong>Talleres de 
Creación de Personajes, Cuento y Crónica</strong>, con el objetivo de mejorar la 
crítica y el autoanálisis de la lectura a través del contacto directo 
con creadores contemporáneos, dichos talleres estuvieron a cargo de 
jóvenes Poetas y Escritores integrantes del “<em>Grupo La Jerga</em>”: <strong>Fryda Ramos, 
Heriberto Rodríguez Macías, Magaly Díaz, Ramsés Anuar Ruíz Ramos y Miguel 
Ángel Delgado Castro</strong>.</p>
<p>Por la tarde, las actividades concluyeron con la presentación de la Revista 
Literaria “<em>La Jerga</em>” publicación independiente creada en el Estado de Nayarit 
con cuatro años de trayectoria, misma que ha sido presentada en diferentes 
Estados como: <strong>Jalisco, Guanajuato, Querétaro y próximamente DF, Chiapas y Sinaloa</strong>.</p>
<p>Al cierre de este evento el Rector de la Institución <strong>Ing. Manuel Quintero</strong> 
Borrego Reconoció el trabajo realizado por la <strong>Lic. María Esthela Valenzuela 
Gutiérrez</strong>, precursora y responsable de la Sala de Lectura de la Institución, 
asimismo agradeció a los integrantes del Grupo La Jerga su presencia en la 
Institución e invitó a la comunidad universitaria a ser  promotores de la lectura.</p>
<p>Concluyó: Con  base en  la lectura se adquieren los conocimientos que nos 
permitirán adquirir las competencias necesarias para desenvolvernos día con día.</p>
<p>Por este medio se hace una cordial invitación a la sociedad en general para
 que  acuda a la Sala de lectura de esta Institución en un horario de 
 8:00 a.m. a 6:00 p.m. de lunes a viernes y disfrute de un buen libro en amplias 
 y cómodas instalaciones, recuerden que las puertas de esta Universidad siempre 
 estarán abiertas.</p>
