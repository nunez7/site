  <div class="col-md-6">
    <p class="m-top-md text-justify">MISIÓN:</p>
    <p>Propiciar, promover y estimular el surgimiento de comunidades con pensamiento propio y crítico que lean de manera libre, gratuita,, incluyente y resignificativa, brindando condiciones materiales, sociales, cognitivas, afectivas y estéticas para que el mayor número posible de personas comparta sus lecturas entre pares y para que los lectores dialoguen, debatan, escriban textos propios derivados de sus lecturas y construyan lazos interpersonales y sociales que fortalezcan el tejido y la vida, capaces de transformar el entorno, impulsando el desarrollo económico, social y cultural de la Zona Norte del Estado de Nayarit.</p>
  </div>
  <div class="col-md-6">
    <p class="m-top-md text-justify">VISIÓN:</p>
    <p>Ser una Sala de Lectura reconocida por su modelo del Programa Nacional de Salas de Lectura. Habitada por comunidades que leen de manera cotidiana, libre, autónoma y compartida, que ofrecen ideas y diversos puntos de vista que invitan al diálogo, a la investigación y al debate, coadyuvando en la aplicación del modelo educativo, social y cultural de la Universidad Tecnológica de la Costa dentro de la Zona Norte, Zona Región Costa Norte y Región Centro del Estado de Nayarit.</p>
