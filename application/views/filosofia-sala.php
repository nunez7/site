    <p class="m-top-md text-justify">CALIDAD SOCIAL E INCLUYENTE:</p>
    <ul>
      <li>Libertad (de pensamiento, de credo, de expresión, de pertenencia y de participación).</li>
      <li>Igualdad (Todas las personas comparten la misma dignidad humana y cada persona tiene derecho a ser ella misma, distinta e irrepetible).</li>
      <li>Inclusión (Todas las personas, sin tener que cambiar sus identidades para ser aceptadas, tienen derecho a participar en la vida ciudadana y a acceder de manera calificada a todos los bienes y servicios disponibles: políticos, materiales, económicos, educativos, sociales y culturales).</li>
      <li>Autonomía (Todas las personas tienen derecho a construir sus propias imágenes de mundos mejores-para ellas y para otros-y a perseguirlas de manera competente al emprender acciones libre y autodiseñadas).</li>
