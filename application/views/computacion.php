<p>Con el objetivo de brindar los conocimientos necesarios para el uso de 
la computadora a nivel básico, este sábado <strong>5 de octubre</strong> inició en la 
<strong>Universidad Tecnológica de la Costa</strong> el <strong>Curso de Computación Básica</strong>, 
dirigido a niños adolescentes y adultos.</p>
<p>Este curso será impartido por el <strong>Ing. Raymundo Raygoza Acosta</strong>, 
egresado de la <strong>Ingeniería en Tecnologías de la Información y Comunicación</strong> 
de dicha Institución Educativa, tendrá un costo de $1400.00 y una duración 
de 4 meses, acudiendo en un horario de 9:00 a.m. a 2:00 p.m. en sistema 
sabatino.</p>
<p>Agregar que los asistentes aprenderán a usar los programas básicos de 
una computadora (<strong>Word, Excel, PowerPoint, etc.</strong>) además del manejo y 
configuración de los  navegadores de internet.</p>
