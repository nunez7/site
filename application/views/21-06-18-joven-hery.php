<p>Este jueves 21 de junio  se llevó a cabo la presentación del Proyecto de Estadía del joven Hery Barbosa Gutiérrez, Excelencia Académica de la Ingeniería en Agrobiotecnología 2018 de la Universidad Tecnológica de la Costa y Mejor Proyecto de Estadía con la investigación: Evaluación Funcional de Bacterias Benéficas para Promover el Crecimiento y Desarrollo Vegetal de Tabaco Tipo Burley (Nicotiana tabacum) para la Empresa British American Tobacco, el cual tiene como objetivo general: Evaluar el efecto de bacterias benéficas nativas sobre el desarrollo vegetal de plantas de tabaco utilizando un sistema productivo de almácigo.


 </p>

<p>Destacar que el proyecto permite disminuir costos de producción, es amigable con el medio ambiente al utilizar fertilizantes orgánicos, además de ahorrar sustancialmente en el consumo de agua y sin duda alguna, será una gran aportación para los productores, al ser el Tabaco uno de las principales cultivos de la Región.


</p>

