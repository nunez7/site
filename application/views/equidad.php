<p>Con el propósito de fomentar la igualdad entre hombres y mujeres de la 
<strong>Universidad Tecnológica de la Costa</strong>, es<strong>te miércoles 9 de octubre en el 
auditorio de vinculación se llevó a cabo la conferencia “El uso no sexista 
del lenguaje”</strong> impartida la <strong>Lic. Heidy Oropeza</strong> del 
<strong>Instituto de la Mujer Nayarita 
 (INMUNAY)</strong> que dirige la <strong>Lic. Luz Amalia Barrón Vivanco</strong>.
En este evento se manejaron diferentes dinámicas que representaron la 
importancia de mantener la igualdad entre hombres y mujeres, se exhortó a los 
jóvenes a compartir los conocimientos adquiridos y así fomentar la <strong>equidad
 de género</strong>.</p>
<p class="reduced">Por su parte el Rector de la Institución <strong>Ing. Manuel Quintero Borrego</strong> 
agradeció la presencia del <strong>Instituto de la Mujer Nayarita (INMUNAY)</strong> 
destacó la importancia que tiene el que este tipo de conferencias se 
realicen con frecuencia en la Institución e invitó al Instituto a 
regresar y ser parte de la formación integral de los alumnos y alumnas 
de <strong>Universidad Tecnológica de la Costa</strong>.</p>
