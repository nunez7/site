<p>La visita guiada en la <strong>Universidad Tecnológica de la Costa</strong>, es una 
actividad coordinada por el   <strong>Departamento de Difusión, Extensión y 
Divulgación Universitaria de la Secretaría de Vinculación</strong>, permite a los
 bachilleratos de la Zona Norte del Estado de Nayarit y Sur de Sinaloa, 
 a través de un recorrido por las instalaciones, conocer las carreras 
 ofertadas, además del modelo educativo de la Institución, en el cual 
 predomina la práctica en laboratorios y talleres con tecnología de punta.</p>
<p>En este sentido, con la visita del <strong>CECYTEN</strong> de San <strong>Andrés Milpillas Municipio 
de Huajicori</strong>,  este viernes 18 de octubre iniciaron de manera oficial en la 
Universidad Tecnológica de la Costa las <em>“Visitas Guiadas”</em>, permitiendo que 
estos jóvenes próximos a egresar del Nivel Medio Superior, pudieran  conocer 
el manejo y funcionalidad de  los Talleres y Laboratorios de: <strong>Cárnicos, Lácteos, 
Microbiología, Análisis Instrumental, Radio, Televisión, Laboratorio de 
Multimedia con equipos iMac, Redes e Idiomas</strong>, solo por mencionar algunos.</p>
<p class="reduced">Al concluir dicho recorrido, padres de familia y docentes que acompañaron 
a estos jóvenes estudiantes, manifestaron su agradecimiento al Rector de la 
Universidad Tecnológica de la Costa, <strong>Ing. Manuel Quintero Borrego</strong>, agregaron 
que esta actividad es de mucho beneficio para los alumnos que están próximos 
a egresar del nivel medio superior, ya que en esta etapa de decisión, 
es importante conocer la oferta educativa de Instituciones de Nivel Superior, 
para que de esta forma los jóvenes se visualicen estudiando una carrera que 
les permita alcanzar el éxito profesional.</p>
