<p>Reconociendo la importancia que tiene el estar estrechamente relacionados 
con el Sector Productivo y con el objetivo de conocer sus necesidades, para 
así trabajar en unidad  y contribuir en el crecimiento y desarrollo de la Z<strong>ona 
Norte del Estado de Nayarit</strong>, este martes 8 de octubre se llevó a cabo una 
sesión de grupo  con empresarios Nayaritas, dicha sesión estuvo dirigida 
por Docentes de la Carrera de <strong>Mercadotecnia</strong> en coordinación con la 
<strong>Secretaría de Vinculación</strong>, el punto de reunión fue la Cámara Gesell 
de la <strong>Universidad Tecnológica de la Costa</strong>.</p>
<p>En este evento se trataron temas como: Factores que influyen en la 
elección de Instituciones de capacitación y el reconocimiento que 
actualmente tiene la UT de la Costa en esta área, se dio a conocer 
el catálogo de servicios y se escucharon las necesidades del sector productivo.
Acudió Personal Directivo de diferentes Instituciones como: <strong>CFE, 
INIFAP, SAGARPA, Monsanto, Caja Popular Villanueva, Caja Popular 
Mexicana, Sello Rojo, Salsas Tepicor y el CBTA 108 de Villa Juárez</strong>.</p>
<p class="reduced">Al cierre de este evento el <strong>Ing. Manuel Quintero Borrego</strong>, Rector de 
la <strong>Universidad Tecnológica de la Costa</strong>, acompañado del <strong>Director de 
Administración y Finanzas</strong> de la Institución, <strong>Rodolfo Rodríguez Abud</strong>, 
agradeció a los empresarios  su asistencia, agregó: <em>“Para la Universidad
 Tecnológica de la Costa es de suma Importancia estar estrechamente  
 relacionados con el Sector Productivo, queremos ser punto de referencia de 
 calidad educativa”</em> en esta Universidad contamos con laboratorios y la tecnología necesaria para la capacitación en el área de agronegocios, esta es una Universidad de todos los Nayaritas.
 Concluyó: <em>“Unidos Universidad  y Sector Productivo Coadyuvaremos en el 
  desarrollo de la Zona Norte del Estado y del País”</em>.</p>
