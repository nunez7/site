		<p class="m-top-md text-justify">¿QUIÉNES SOMOS?</p>
		<p>	
			Desde el 10 de octubre de 2012 <strong>la Universidad Tecnológica de la Costa ha figurado a Nivel Nacional por ser la única dentro del Subsistema de Universidades Tecnológicas y Politécnicas que cuenta con una Sala de Lectura, considerada como prototipo</strong>, la cual  posee la Certificación: <strong>SL18015010</strong> emitida por la Universidad Autónoma Metropolitana y la Secretaria de Cultura. 

		</p>
		<p>
			Pertenece: Programa Nacional de Salas de Lectura y Fomento a la Lectura y Escritura de la Secretaria de Cultura.
			En la Sala de Lectura: La “Piedra en el Estanque” existen 3,350 libros se enumeran  15 E-books Sony Reader Tablet,  los cuales cuentan con un banco de 6,500 PDF, de obras literarias de todas las categorías de la Literatura Universal.

		</p>
		<p>
			Se cuenta con una Videoteca/bilingüe  con una colección organizada física y digitalmente.
			Tiene en su acervo  literatura en Lenguas Indígenas Nacionales, además en el idioma inglés y francés.

		</p>
		<p>
			La Sala de Lectura se encuentra abierta a la comunidad de La zona Norte, Costa Norte y Región Centro del Estado de Nayarit, se han atendido a los niveles básicos y medio superior.

		</p>
		<p>
			Además en ella se imparten: Talleres de Creación Literaria, Fomento a la Lectura y Escritura, Poesía, Teatro, Pintura a Escuelas Públicas y Privadas de Nivel básico (preescolar, primaria, secundaria), Nivel medio superior (preparatorias, CBTAS, CECYTEN, Tele prepas), y comunidad en general.

		</p>
