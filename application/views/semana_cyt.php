<p>Con el propósito de despertar el interés por las disciplinas científicas 
y tecnológicas entre el público infantil y juvenil, propiciando un 
acercamiento entre científicos, divulgadores, investigadores, empresarios, 
tecnólogos y autoridades participantes, en un escenario de cordialidad y 
respeto a las nuevas generaciones y con la  misión de promover la ciencia 
y proyectarla como pilar fundamental del desarrollo económico, cultural 
y social de nuestro País, este lunes 20 de octubre iniciaron en la UT de 
la Costa las actividades en conmemoración del <strong>20 Aniversario de la 
Semana Nacional de  Ciencia y Tecnología</strong>.</p>
<p>El encargado de inaugurar estas actividades fue el Rector de la Institución 
<strong>Ing. Manuel Quintero Borrego</strong>, acompañado de <strong>Lic. Jorge Gutiérrez Mata</strong>, 
quien acudió en representación del <strong>Dr. Pavel Plata Jarero</strong>, Director 
General del <strong>Consejo de Ciencia y Tecnología del Estado de Nayarit 
(COCYTEN)</strong>, asimismo acompañaron al Rector: Personal Directivo, 
Administrativos, Docentes y Alumnos.</p>
<p>Una vez iniciado el acto protocolario de inauguración se dio paso a 
la Conferencia Magistral “<em>La Innovación y los Valores: Dos Ejes Rectores 
en la Sociedad del Conocimiento</em>” impartida por el  <strong>Dr. Gerardo Torres 
García</strong>, Responsable de la Oficina de Gestión del <strong>CIAD</strong> (Centro de 
Investigación en Alimentos y Desarrollo A.C.) Tepic.</p>
<p>El resto de la semana Docentes de la UT de la Costa Impartirán las 
siguientes Conferencias Técnicas: 22 de octubre, el <strong>Ing. Pedro Vázquez 
Vázquez</strong> impartirá la conferencia  "<em>La Investigación Científica en las 
Instituciones de Educación Superior</em>"  y la <strong>Ing.  Merle Catalina  
Navarro Cortéz</strong>.-"<em>Tecnologías para el Procesamiento Aséptico de los Alimentos</em>", 
el 23 de octubre <strong>M.G.T.I. José Armando Medina Aguilar</strong>.- "<em>Las TIC's en un 
Mundo Juvenil y Productivo</em>" y 24 de octubre- <strong>M.C. Manuel Eduardo Morales 
Rubio</strong>.- “<em>La Importancia del Comercio Internacional en el País</em>”, conferencias 
que estarán dirigidas a jóvenes estudiantes del <strong>CBTA 132 de Guadalupe Victoria</strong>, 
<strong>Prepa 2 de Santiago Ixcuintla</strong>, <strong>Secundaria Federal  Leyes de Reforma</strong>, 
<strong>Primarias del Mpio.</strong>  y a la Comunidad Universitaria de UTC.</p>
<p class="reduced">Agregar que a este festejo nacional se suman además de Instituciones 
Educativas, diferentes  Asociaciones Científicas, Secretarías de Estado, 
Empresas, Centros de Investigación, Museos de Ciencia y Gobiernos Estatales. 
Se concreta en eventos creativos y propositivos de científicos, maestros, 
divulgadores y empresarios mediante ciclos de conferencias, talleres, 
exposiciones, demostraciones, visitas guiadas, concursos y ferias 
científicas, entre otros.</p>