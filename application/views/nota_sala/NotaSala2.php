<p>Con el objetivo de dar continuidad al Taller de Creación Literaria: TEWI NIUKIYARI (Palabra de la Gente), el cual está por cumplir un año, este miércoles 24 de enero, la Sala de Lectura " La piedra en el estanque" recibió a un grupo de chicos entre 12 y 14 años de edad de la Secundaria Francisco J. Múgica de Amapa Mpio. de Santiago Ixcuintla. 
</p>

<p>En este taller los asistentes llevaron a cabo una dinámica denominada  párrafos locos con el cuento "La vieja que engaño a la muerte", además tuvieron la oportunidad de leer, platicar, planear la próxima visita a la sala de lectura y llevarse en calidad de préstamo un libro para ser entregado en la próxima visita.
</p>
 
<p>Disfruten su lectora jóvenes, nos los esperamos el próximo mes.
</p>

