<p>Con el objetivo de que UT de la Costa a través de la Carrera de Turismo Alternativo, se incorpore en proyectos relacionados  con el Crecimiento del Turismo de Naturaleza en el Estado, este miércoles 18 de julio el Rector de UTC L.A.E. Miguel Salinas Ramírez  acompañado del Cuerpo Directivo de la Institución, sostuvo una plática con el Secretario de Turismo de Nayarit, C. Eulalio Coronado Mendoza, lo anterior,  en la Entrega de Constancias de Acreditación  de Guías de Turistas en la NOM09TUR-2002 de Excursionismo, evento realizado en cumplimiento al PED 2017-2021, en el Desarrollo Ecoturístico Salto Negro en San José de Gracia Municipio de Ixtlán del Río. 

 </p>

<p>Destacar que en este evento la Maestra Claudia Medina Aguilar, actualmente Docente de UT de la Costa y Propietaria de la Empresa Travesía Cora recibió dicha acreditación.


</p>

<p>Aquí se trabaja día a día para fortalecer la Carrera de Turismo, que esperas para inscribirte y ser parte de la Familia UTC. 
