<p>Con el objetivo de incrementar los índices de lectura en el 100% de 
la planta docente, estudiantes, personal administrativo y directivo de 
la  <strong>Universidad Tecnológica de la Costa</strong>, además de realizar diferentes 
talleres que les permitan encontrar el placer por la lectura, el día 10 
de octubre de 2012  dentro del marco de los festejos del Décimo 
Aniversario de la Institución, fue creada la <em>“Sala de Lectura  La Piedra 
en el Estanque”</em> como parte del proyecto: <strong>Universidad Lectora,  
con un recurso de $140, 000.00  del Programa  de Apoyo al Desarrollo 
de la Educación Superior (PADES)</strong>.</p>
<p>El encargado de realizar dicha inauguración fue el Rector de la 
Institución <strong>Ing. Manuel Quintero Borrego</strong> acompañado de Autoridades 
Estatales  y a un año de creada esta sala, la Comunidad Universitaria 
continúa disfrutando de la lectura de un buen libro en cómodas instalaciones 
donde se pueden encontrar diferentes géneros literarios.</p>
<p>Se tiene como meta anual incrementar el índice de lectura a 5 libros, 
así como realizar 6 conferencias talleres con escritores y poetas.</p>
<p class="reduced">Agregar que este curso se hace posible gracias al <em>“Proyecto realizado con financiamiento de la Secretaría de Educación Pública-Subsecretaría de Educación Superior-Dirección General de Educación Superior Universitaria”</em>, con número de convenio <strong>2013-18-004-077</strong>.</p>
