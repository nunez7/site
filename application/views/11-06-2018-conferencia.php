<p>Este viernes 8 de junio, se llevó a cabo la conferencia “Tu Futuro está en las T.I.” impartida por Cruz Felipe Rodríguez López, Egresado de la Carrera de Tecnologías de la Información y Comunicación, Área Sistemas Informáticos,  quien actualmente se desempeña como Desarrollador de Software en la Empresa Komatsu de Saltillo Coahuila.
 </p>

<p>El encargado de dar la bienvenida fue el Rector de la Institución, L.A.E. Miguel Salinas Ramírez, quien dijo estar orgulloso de recibir a Cruz Felipe como egresado de calidad UTC, además destacó una de sus  grandes aportaciones a la Institución, el SIEst (Sistema Integral Estudiantil) el cual es actualmente utilizado en la Institución.
</p>

<p>En este evento, Cruz Felipe compartió su experiencia estudiantil, así como laboral y  exhortó a los jóvenes estudiantes a desarrollar su proyecto de vida, estar al día y la vanguardia con las nuevas tecnologías y aprovechar al máximo los conocimientos adquiridos en la Institución.
 </p>

