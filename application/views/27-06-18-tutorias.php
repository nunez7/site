<p>Este miércoles 27 de junio el Rector de la Universidad Tecnológica de la Costa, Miguel Salinas Ramírez dio por iniciados los trabajos del curso de capacitación para la implementación de un nuevo Programa Institucional de Tutorías, que en atención a los lineamientos establecidos por la CGUTyP, tiene como objetivo establecer un Modelo Nacional que homologue las funciones, procesos y lineamientos de operación de las personas involucradas en la acción tutorial y de esta manera, contribuir al fortalecimiento del Subsistema de Universidades Tecnológicas y Politécnicas.

 </p>

<p>En la apertura de dicho curso, el Rector de la Institución manifestó su agradecimiento al Facilitador M.G.T.I. José Armando Medina Aguilar, Profesor de Tiempo Completo de la Institución, quien cuenta con amplia trayectoria en el tema y a los Docentes por su participación en las actividades de actualización.


</p>

<p>Manifestó estar seguro que a través del trabajo en equipo y la implementación del nuevo Programa de Tutorías, se cumplirá el propósito y se logrará disminuir los indicadores de deserción.


 </p>
