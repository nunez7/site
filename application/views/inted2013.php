<p>El Estado de Puebla fue sede el <strong>2do. Congreso Internacional de 
Tecnologías de la Información y Comunicación de las Universidades 
Tecnológicas y Politécnicas INTED 2013</strong>, celebrado del 9 al 11 de octubre, 
evento al  que acudieron alumnos  de la <strong>División de Ingeniería Ciencia y 
Tecnología de la UTC</strong>, además Docentes de esta misma Institución, 
impartieron una serie de talleres dirigidos a estudiantes de nivel superior.
Fue así como el <strong>TSU. Cruz Felipe Rodríguez López</strong>, Egresado y Docente  
de la Universidad Tecnológica de la Costa impartió el taller: <strong>Desarrollo 
de Aplicaciones Móviles en Android: Navegación entre interfaces</strong>, 
mientras que el <strong>M.C.E. Héctor Hugo Domínguez Jaime</strong> impartió los talleres: 
<strong>Uso de las TIC en Procesos Educativos</strong> y <strong>Comunicaciones Unificadas Cisco</strong>.</p>
<p>Al respecto el <strong>Ing. Manuel Quintero Borrego Rector de la Universidad 
Tecnológica de la Costa</strong>  al ser entrevistado declaró: “<em>Me siento 
satisfecho de que nuestros alumnos tengan participación en congresos 
que les permitan adquirir nuevos conocimientos como parte de su formación 
profesional, pero más satisfecho, cuando nuestros docentes destacan en 
este tipo de actividades y demuestran la calidad educativa que se 
tiene en la Institución</em>”.</p>
<p class="reduced">Agregar que dichos  talleres además de la conferencia “<em>Aplicaciones 
de las TIC´S en el Campo"</em> se tienen contemplados para impartirse los 
días 22 y 23 de octubre en el <strong>Instituto Tecnológico del Norte de 
Nayarit</strong>, dentro del marco de festejos de la <strong>Semana de Ciencia y Tecnología</strong>.</p>