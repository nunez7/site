<p>El Rector de La Universidad Tecnológica de la Costa <strong>Ing. Manuel Quintero 
	Borrego</strong> da a conocer que el <strong>Departamento de Prácticas y Estadías</strong> ya se 
	encuentra realizando los trámites correspondientes para que alumnos de la 
	Institución de Nivel Ingeniería realicen su estadía durante el periodo 
	<strong>Enero-Abril</strong>, cubriendo un total de 480 horas en empresas del Estado y del 
	País, en esta ocasión  participarán las <strong>Ingenierías en Desarrollo e Innovación 
	Empresarial, Procesos Alimentarios, Procesos Biotecnológicos y  Tecnologías de 
	la Información y Comunicación</strong>.</p>
<p>Recordar que la <strong>estadía</strong> es un periodo de práctica empresarial donde los alumnos 
próximos a egresar aplican sus conocimientos a través de un proyecto específico 
en <strong>Instituciones Públicas y Privadas</strong>, enfrentando situaciones laborales reales y 
los más rescatable es que la mayor parte de los jóvenes que realizan esta actividad,
 debido a su desempeño, son contratados rápidamente ocupando puestos importantes 
 dentro de las empresas.</p>
<p class="reduced">Vale la pena agregar que para esta actividad primeramente se realiza un acercamiento 
entre Universidad y Empresa, se detecta alguna necesidad o problemática y se propone 
un proyecto realizar, posterior se evalúa el perfil del alumno de acuerdo al proyecto, se designa un asesor por parte de la empresa, quien evaluará el desempeño del alumno y a su vez será el contacto directo con el asesor académico designado por la Universidad.
